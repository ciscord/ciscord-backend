import { objectType } from 'nexus'
import { Message, ReplyMessage } from '../index'

export const RemoteAttachment = objectType({
  name: 'RemoteAttachment',
  definition(t) {
    t.string('description')
    t.nonNull.string('id')
    t.field('parentMessage', { type: Message })
    t.field('parentReplyMessage', { type: ReplyMessage })
    t.string('preview')
    t.string('siteName')
    t.nonNull.string('title')
    t.nonNull.string('url')
    t.string('videoLink')
  }
})
